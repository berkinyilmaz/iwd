export const getRandomWoman = () => {
  const randomIndex = Math.floor(Math.random() * women.length);
  return women[randomIndex];
};

export const getWomanOfTheDay = () => {
  const today = new Date();
  const dayOfYear = Math.floor(
    (today - new Date(today.getFullYear(), 0, 0)) / (1000 * 60 * 60 * 24)
  );
  const index = dayOfYear % women.length;
  return women[index];
};

export const getWomenByTimeline = () => {
  return [...women].sort((a, b) => a.birthYear - b.birthYear);
};

export const getWomanById = (id) => {
  return women.find(w => w.id === id);
};
